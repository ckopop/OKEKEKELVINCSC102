# Integer number
num = 100
print(num)  # Prints the value of 'num'
print("Data Type of variable num is", type(num))  # Prints the type of 'num'

# float number
fnum = 34.45
print(fnum)  # Prints the value of 'fnum'
print("Data Type of variable fnum is", type(fnum))  # Prints the type of 'fnum'

# complex number
cnum = 3 + 4j
print(cnum)  # Prints the value of 'cnum'
print("Data Type of variable cnum is", type(cnum))  # Prints the type of 'cnum'
